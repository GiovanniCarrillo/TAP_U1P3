    public void guardar(String name,String a1, String a2, String sex, String memb, String act){
        String dataArchivo = "";
        dataArchivo = "Nombre: "+name+" "+a1+" "+a2+"\n"+"Sexo:"+sex+"\nTipo de Membresia:"+memb+"\nActividades inscritas: "+act;

        String nameArch = name+".txt";
        try{
            OutputStreamWriter archivo = new OutputStreamWriter(new FileOutputStream(nameArch));
            archivo.write(dataArchivo);
            archivo.flush();
            archivo.close();
            JOptionPane.showMessageDialog(this, "SU REGISTRO HA SIDO CON ÉXITO");        
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
