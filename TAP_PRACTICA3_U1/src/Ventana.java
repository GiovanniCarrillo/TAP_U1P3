    private void btnModoOscuroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModoOscuroActionPerformed
        if(btnModoOscuro.isSelected() == true){
            panelRegistro.setBackground(Color.lightGray);
        }else{
            panelRegistro.setBackground(Color.WHITE);
        }
        
        cont = cont+1;
        
        if(cont>10){
            this.setTitle("YA FUNDISTE EL PROGRAMA");
            panelRegistro.setBackground(Color.BLACK);
            jpanel2.setBackground(Color.BLACK);
            int resp = showConfirmDialog(this, "Se fundio el programa, deseas reiniciar?","Alerta!",
                YES_NO_OPTION,
                ERROR_MESSAGE);
        
        if(resp==0){
            panelRegistro.setBackground(Color.WHITE);
            jpanel2.setBackground(Color.WHITE);
            cont = 0;
            this.setTitle("");
        }else{
            System.exit(0);
        }
        
        }
    }//GEN-LAST:event_btnModoOscuroActionPerformed

